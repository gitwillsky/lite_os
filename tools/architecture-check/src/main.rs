struct SourceFile {
    relative: String,
    owner: String,
    text: String,
    lines: Vec<String>,
    syntax: File,
    binary_crate: bool,
}

impl SourceFile {
    fn at(&self, line: usize) -> String {
        format!("{}:{line}", self.relative)
    }

    fn preceding_contains(&self, line: usize, distance: usize, marker: &str) -> bool {
        let start = line.saturating_sub(distance + 1);
        self.lines[start..line.saturating_sub(1).min(self.lines.len())]
            .iter()
            .any(|candidate| candidate.contains(marker))
    }
}

fn main() -> ExitCode {
    let write_interface = env::args().skip(1).any(|arg| arg == "--write-interface");
    let root = repository_root();
    let mut errors = Vec::new();
    let mut review_notices = Vec::new();
    let sources = match load_sources(&root) {
        Ok(sources) => sources,
        Err(error) => {
            eprintln!("architecture fence failed:\n- {error}");
            return ExitCode::FAILURE;
        }
    };

    check_dependencies(&root, &sources, &mut errors);
    check_source_sizes(&root, &sources, &mut errors, &mut review_notices);
    check_visibility(&sources, &mut errors);
    check_source_patterns(&root, &sources, &mut errors);
    check_ready_projection_consumers(&sources, &mut errors);
    check_ready_transition_shapes(&sources, &mut errors);
    check_fallible_collections(&root, &sources, &mut errors);
    check_global_owners(&sources, &mut errors);
    check_unsafe_proofs(&sources, &mut errors);
    check_abi(&root, &mut errors);
    check_documentation(&root, &mut errors);
    check_userspace_single_track(&root, &mut errors);

    let surface = interface_surface(&sources);
    if write_interface {
        if errors.is_empty() {
            let baseline = root.join("docs/architecture-interface.txt");
            let content = format!("{INTERFACE_HEADER}{}\n", surface.join("\n"));
            if let Err(error) = fs::write(&baseline, content) {
                errors.push(format!("failed to write {}: {error}", baseline.display()));
            }
        }
    } else {
        check_interface_baseline(&root, &surface, &mut errors);
    }

    if !review_notices.is_empty() {
        eprintln!("architecture review required:");
        for notice in review_notices {
            eprintln!("- {notice}");
        }
    }

    if errors.is_empty() {
        println!("architecture fence passed");
        ExitCode::SUCCESS
    } else {
        eprintln!("architecture fence failed:");
        for error in errors {
            eprintln!("- {error}");
        }
        ExitCode::FAILURE
    }
}

fn repository_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(Path::parent)
        .expect("architecture-check must live under tools/")
        .to_path_buf()
}

struct SourceSizeReview {
    limit: usize,
    owner: String,
    reason: String,
    exit: String,
}

fn source_size_reviews(root: &Path) -> Result<BTreeMap<String, SourceSizeReview>, String> {
    let path = root.join("docs/architecture-contract.md");
    let text = fs::read_to_string(&path)
        .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
    let mut reviews = BTreeMap::new();
    for line in text.lines() {
        let columns: Vec<&str> = line.split('|').map(str::trim).collect();
        if columns.len() < 7 || !columns[1].starts_with('`') {
            continue;
        }
        let relative = columns[1].trim_matches('`');
        if !relative.starts_with("kernel/src/") && !relative.starts_with("bootloader/src/") {
            continue;
        }
        let Ok(limit) = columns[2].parse::<usize>() else {
            continue;
        };
        let review = SourceSizeReview {
            limit,
            owner: columns[3].trim_matches('`').to_owned(),
            reason: columns[4].to_owned(),
            exit: columns[5].to_owned(),
        };
        if review.limit <= SOURCE_REVIEW_LINE_THRESHOLD {
            return Err(format!(
                "{relative}: source-size review must exceed the {SOURCE_REVIEW_LINE_THRESHOLD}-line review threshold"
            ));
        }
        if review.owner.is_empty() || review.reason.is_empty() || review.exit.is_empty() {
            return Err(format!(
                "{relative}: source-size review requires owner, reason and exit criterion"
            ));
        }
        if reviews.insert(relative.to_owned(), review).is_some() {
            return Err(format!("duplicate source-size review for {relative}"));
        }
    }
    Ok(reviews)
}

fn check_source_sizes(
    root: &Path,
    sources: &[SourceFile],
    errors: &mut Vec<String>,
    review_notices: &mut Vec<String>,
) {
    let reviews = match source_size_reviews(root) {
        Ok(reviews) => reviews,
        Err(error) => {
            errors.push(error);
            return;
        }
    };
    let known: BTreeSet<&str> = sources
        .iter()
        .map(|source| source.relative.as_str())
        .collect();
    for relative in reviews.keys() {
        if !known.contains(relative.as_str()) {
            errors.push(format!(
                "{relative}: source-size review does not name a production Rust source"
            ));
        }
    }
    for source in sources {
        let lines = source.lines.len();
        let review = reviews.get(&source.relative);
        if let Some(review) = review
            && review.owner != source.owner
            && !review.owner.starts_with(&format!("{}::", source.owner))
        {
            errors.push(format!(
                "{}: source-size review owner `{}` does not belong to module `{}`",
                source.relative, review.owner, source.owner
            ));
        }
        let limit = review.map_or(DEFAULT_SOURCE_REJECTION_LIMIT, |review| review.limit);
        if lines > limit {
            let detail = if review.is_some() {
                format!("reviewed source-file limit {limit}")
            } else {
                format!("default rejection limit {DEFAULT_SOURCE_REJECTION_LIMIT}")
            };
            errors.push(format!(
                "{}: {lines} lines exceeds its {detail}; split at a domain seam or record an exact reviewed limit in docs/architecture-contract.md",
                source.relative
            ));
        } else if review.is_some() && lines < limit {
            errors.push(format!(
                "{}: source shrank to {lines} lines; lower its architecture limit from {limit} to preserve the ratchet",
                source.relative
            ));
        } else if lines > SOURCE_REVIEW_LINE_THRESHOLD && review.is_none() {
            review_notices.push(format!(
                "{}: {lines} lines exceeds the {SOURCE_REVIEW_LINE_THRESHOLD}-line review threshold; review its owner/seam and either split it or add owner, reason and exit criterion to docs/architecture-contract.md",
                source.relative
            ));
        }
    }
}


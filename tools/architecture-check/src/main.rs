fn check_user_source_sizes(root: &Path, errors: &mut Vec<String>) {
    let mut pending = vec![root.join("user")];
    while let Some(directory) = pending.pop() {
        let entries = match fs::read_dir(&directory) {
            Ok(entries) => entries,
            Err(error) => {
                errors.push(format!(
                    "failed to inspect {}: {error}",
                    directory.display()
                ));
                continue;
            }
        };
        for entry in entries.flatten() {
            let path = entry.path();
            if path.is_dir() {
                pending.push(path);
                continue;
            }
            let extension = path.extension().and_then(|extension| extension.to_str());
            if !matches!(
                extension,
                Some("rs" | "c" | "h" | "js" | "mjs" | "ts" | "tsx" | "css")
            ) {
                continue;
            }
            let Ok(source) = fs::read_to_string(&path) else {
                errors.push(format!("failed to read user source {}", path.display()));
                continue;
            };
            let lines = source.lines().count();
            if lines > SOURCE_REVIEW_LINE_THRESHOLD {
                let relative = path.strip_prefix(root).unwrap_or(&path).display();
                errors.push(format!(
                    "{relative}: {lines} lines exceeds the hard {SOURCE_REVIEW_LINE_THRESHOLD}-line user-module limit; split at an owner/interface seam"
                ));
            }
        }
    }
}

fn rust_files(directory: &Path, files: &mut Vec<PathBuf>) -> Result<(), String> {
    let entries = fs::read_dir(directory)
        .map_err(|error| format!("failed to read {}: {error}", directory.display()))?;
    for entry in entries {
        let path = entry
            .map_err(|error| format!("failed to read directory entry: {error}"))?
            .path();
        if path.is_dir() {
            rust_files(&path, files)?;
        } else if path.extension().is_some_and(|extension| extension == "rs") {
            files.push(path);
        }
    }
    Ok(())
}

fn load_sources(root: &Path) -> Result<Vec<SourceFile>, String> {
    let mut sources = Vec::new();
    for domain in SOURCE_DOMAINS {
        let source_root = root.join(domain.root);
        let mut paths = Vec::new();
        rust_files(&source_root, &mut paths)?;
        paths.sort();
        for path in paths {
            let text = fs::read_to_string(&path)
                .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
            let syntax = syn::parse_file(&text)
                .map_err(|error| format!("failed to parse {}: {error}", path.display()))?;
            let relative_path = path
                .strip_prefix(root)
                .map_err(|error| format!("invalid source path {}: {error}", path.display()))?;
            let owner = module_owner(relative_path, domain.root);
            sources.push(SourceFile {
                relative: relative_path.to_string_lossy().replace('\\', "/"),
                owner,
                lines: text.lines().map(str::to_owned).collect(),
                text,
                syntax,
                binary_crate: domain.binary_crate,
            });
        }
    }
    Ok(sources)
}

fn module_owner(relative: &Path, source_root: &str) -> String {
    let inside = relative
        .strip_prefix(source_root)
        .expect("source path must be below its source root");
    let first = inside
        .components()
        .next()
        .expect("Rust source path is not empty");
    let name = first.as_os_str().to_string_lossy();
    name.strip_suffix(".rs").unwrap_or(&name).to_owned()
}

#[derive(Default)]
struct PathCollector {
    paths: Vec<Vec<String>>,
    crate_root_alias: bool,
}

impl<'ast> Visit<'ast> for PathCollector {
    fn visit_path(&mut self, path: &'ast SynPath) {
        self.paths.push(
            path.segments
                .iter()
                .map(|segment| segment.ident.to_string())
                .collect(),
        );
        syn::visit::visit_path(self, path);
    }

    fn visit_item_use(&mut self, item: &'ast ItemUse) {
        self.crate_root_alias |= aliases_crate_root(&item.tree);
        expand_use_tree(Vec::new(), &item.tree, &mut self.paths);
    }
}

fn aliases_crate_root(tree: &UseTree) -> bool {
    match tree {
        UseTree::Rename(rename) => rename.ident == "crate",
        UseTree::Name(name) => name.ident == "crate",
        UseTree::Path(path) if path.ident == "crate" => matches!(*path.tree, UseTree::Glob(_)),
        _ => false,
    }
}

fn expand_use_tree(prefix: Vec<String>, tree: &UseTree, paths: &mut Vec<Vec<String>>) {
    match tree {
        UseTree::Path(path) => {
            let mut next = prefix;
            next.push(path.ident.to_string());
            expand_use_tree(next, &path.tree, paths);
        }
        UseTree::Name(name) => {
            let mut path = prefix;
            path.push(name.ident.to_string());
            paths.push(path);
        }
        UseTree::Rename(rename) => {
            let mut path = prefix;
            path.push(rename.ident.to_string());
            paths.push(path);
        }
        UseTree::Glob(_) => paths.push(prefix),
        UseTree::Group(group) => {
            for item in &group.items {
                expand_use_tree(prefix.clone(), item, paths);
            }
        }
    }
}

fn allowed_dependencies(root: &Path) -> Result<BTreeMap<String, BTreeSet<String>>, String> {
    let path = root.join("docs/architecture-contract.md");
    let text = fs::read_to_string(&path)
        .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
    let mut rules = BTreeMap::new();
    for line in text.lines() {
        let columns: Vec<&str> = line.split('|').map(str::trim).collect();
        if columns.len() < 5 || !columns[1].starts_with('`') {
            continue;
        }
        let owner = columns[1].trim_matches('`');
        if !KERNEL_MODULES.contains(&owner) {
            continue;
        }
        let mut allowed = BTreeSet::new();
        let mut remainder = columns[2];
        while let Some(start) = remainder.find('`') {
            remainder = &remainder[start + 1..];
            let Some(end) = remainder.find('`') else {
                return Err(format!("{owner}: malformed dependency matrix row"));
            };
            let dependency = &remainder[..end];
            if !KERNEL_MODULES.contains(&dependency) {
                return Err(format!(
                    "{owner}: dependency matrix names unknown module `{dependency}`"
                ));
            }
            allowed.insert(dependency.to_owned());
            remainder = &remainder[end + 1..];
        }
        if rules.insert(owner.to_owned(), allowed).is_some() {
            return Err(format!(
                "docs/architecture-contract.md contains duplicate dependency rows for `{owner}`"
            ));
        }
    }
    for module in KERNEL_MODULES {
        if !rules.contains_key(*module) {
            return Err(format!(
                "docs/architecture-contract.md lacks a dependency row for `{module}`"
            ));
        }
    }
    Ok(rules)
}

fn check_dependencies(root: &Path, sources: &[SourceFile], errors: &mut Vec<String>) {
    let allowlist = match allowed_dependencies(root) {
        Ok(allowlist) => allowlist,
        Err(error) => {
            errors.push(error);
            return;
        }
    };
    let known: BTreeSet<&str> = KERNEL_MODULES.iter().copied().collect();
    for source in sources
        .iter()
        .filter(|source| source.relative.starts_with("kernel/src/"))
    {
        let mut collector = PathCollector::default();
        collector.visit_file(&source.syntax);
        if collector.crate_root_alias {
            errors.push(format!(
                "{}: aliasing or glob-importing the crate root bypasses dependency ownership",
                source.relative
            ));
        }
        let mut dependencies = BTreeSet::new();
        for path in &collector.paths {
            if path.first().is_some_and(|segment| segment == "crate")
                && let Some(module) = path.get(1)
                && known.contains(module.as_str())
                && module != &source.owner
            {
                dependencies.insert(module.as_str());
            }
            check_facade_path(source, path, errors);
        }
        let Some(allowed) = allowlist.get(source.owner.as_str()) else {
            errors.push(format!(
                "{}: kernel module `{}` has no positive dependency contract",
                source.relative, source.owner
            ));
            continue;
        };
        for dependency in dependencies {
            if allowed.contains(dependency) {
                continue;
            }
            errors.push(format!(
                "{}: crate::{} is absent from the positive dependency contract for {}",
                source.relative, dependency, source.owner
            ));
        }
    }

    for source in sources
        .iter()
        .filter(|source| source.relative.starts_with("bootloader/src/"))
    {
        if source.text.contains("kernel::") || source.text.contains("user::") {
            errors.push(format!(
                "{}: bootloader must remain an independent M-mode domain",
                source.relative
            ));
        }
    }
}

fn check_facade_path(source: &SourceFile, path: &[String], errors: &mut Vec<String>) {
    if path.first().is_none_or(|segment| segment != "crate") {
        return;
    }
    if source.owner == "fs"
        && path.get(1).is_some_and(|segment| segment == "drivers")
        && path.get(2).is_none_or(|segment| segment != "block")
    {
        errors.push(format!(
            "{}: filesystem may depend only on the drivers::block seam",
            source.relative
        ));
    }
    if source.owner == "syscall" {
        if matches!(
            path.get(1).map(String::as_str),
            Some("fs" | "memory" | "task")
        ) && path.len() == 2
        {
            errors.push(format!(
                "{}: syscall must import a named domain facade item, not alias its module root",
                source.relative
            ));
        }
        let forbidden = [
            &["crate", "fs", "ext2"][..],
            &["crate", "fs", "file"][..],
            &["crate", "fs", "inode"][..],
            &["crate", "fs", "Ext2FileSystem"][..],
            &["crate", "memory", "page_table"][..],
            &["crate", "task", "scheduler"][..],
        ];
        if forbidden.iter().any(|prefix| {
            path.len() >= prefix.len()
                && path
                    .iter()
                    .map(String::as_str)
                    .zip(prefix.iter().copied())
                    .all(|(actual, expected)| actual == expected)
        }) {
            errors.push(format!(
                "{}: syscall bypasses a domain facade through {}",
                source.relative,
                path.join("::")
            ));
        }
    }
    if source.owner == "task"
        && path.get(1).is_some_and(|segment| segment == "fs")
        && path
            .get(2)
            .is_some_and(|segment| segment == "Ext2FileSystem")
    {
        errors.push(format!(
            "{}: task may consume filesystem interfaces but not the ext2 adapter",
            source.relative
        ));
    }
}

fn is_scoped_visible(visibility: &Visibility) -> bool {
    matches!(visibility, Visibility::Restricted(restricted) if !restricted.path.is_ident("self"))
}

fn is_bare_public(visibility: &Visibility) -> bool {
    matches!(visibility, Visibility::Public(_))
}

fn normalized(tokens: impl ToTokens) -> String {
    tokens.into_token_stream().to_string()
}

struct SurfaceCollector<'a> {
    relative: &'a str,
    entries: Vec<String>,
    errors: Option<&'a mut Vec<String>>,
    binary_crate: bool,
    owner: Vec<String>,
}

impl SurfaceCollector<'_> {
    fn record(&mut self, declaration: String) {
        self.entries
            .push(format!("{} :: {}", self.relative, declaration.trim()));
    }

    fn visibility(&mut self, visibility: &Visibility, span: Span, declaration: String) -> bool {
        if self.binary_crate
            && is_bare_public(visibility)
            && let Some(errors) = self.errors.as_deref_mut()
        {
            errors.push(format!(
                "{}:{}: binary-crate implementation must use scoped visibility",
                self.relative,
                span.start().line
            ));
        }
        if is_scoped_visible(visibility) {
            self.record(format!("{} {declaration}", normalized(visibility)));
            true
        } else {
            false
        }
    }

    fn owner_name(&self) -> String {
        self.owner.join("::")
    }

    fn signature(&mut self, visibility: &Visibility, signature: &Signature, span: Span) {
        let owner = self.owner_name();
        let signature = normalized(signature);
        let declaration = if owner.is_empty() {
            signature
        } else {
            format!("impl {owner} :: {signature}")
        };
        self.visibility(visibility, span, declaration);
    }
}

impl<'ast> Visit<'ast> for SurfaceCollector<'_> {
    fn visit_item_fn(&mut self, item: &'ast ItemFn) {
        self.signature(&item.vis, &item.sig, item.sig.ident.span());
    }

    fn visit_item_struct(&mut self, item: &'ast ItemStruct) {
        let name = item.ident.to_string();
        let declaration = format!("struct {} {}", item.ident, normalized(&item.generics));
        self.visibility(&item.vis, item.ident.span(), declaration);
        self.owner.push(name);
        for field in &item.fields {
            self.visit_field(field);
        }
        self.owner.pop();
    }

    fn visit_item_union(&mut self, item: &'ast ItemUnion) {
        let declaration = format!("union {}", item.ident);
        self.visibility(&item.vis, item.ident.span(), declaration);
        self.owner.push(item.ident.to_string());
        for field in &item.fields.named {
            self.visit_field(field);
        }
        self.owner.pop();
    }

    fn visit_field(&mut self, field: &'ast Field) {
        let name = field
            .ident
            .as_ref()
            .map_or_else(|| "<tuple>".to_owned(), ToString::to_string);
        let owner = self.owner_name();
        let ty = normalized(&field.ty);
        self.visibility(
            &field.vis,
            field
                .ident
                .as_ref()
                .map_or_else(Span::call_site, |id| id.span()),
            format!("{owner} :: {name} : {ty}"),
        );
    }

    fn visit_item_enum(&mut self, item: &'ast ItemEnum) {
        if self.visibility(
            &item.vis,
            item.ident.span(),
            format!("enum {} {}", item.ident, normalized(&item.generics)),
        ) {
            for variant in &item.variants {
                self.record(format!("enum {} :: {}", item.ident, normalized(variant)));
            }
        }
    }

    fn visit_item_trait(&mut self, item: &'ast ItemTrait) {
        if self.visibility(
            &item.vis,
            item.ident.span(),
            format!("trait {} {}", item.ident, normalized(&item.generics)),
        ) {
            for trait_item in &item.items {
                let declaration = match trait_item {
                    TraitItem::Fn(function) => normalized(&function.sig),
                    TraitItem::Const(constant) => {
                        format!("const {} : {}", constant.ident, normalized(&constant.ty))
                    }
                    TraitItem::Type(ty) => {
                        format!("type {} {}", ty.ident, normalized(&ty.generics))
                    }
                    other => normalized(other),
                };
                self.record(format!("trait {} :: {declaration}", item.ident));
            }
        }
    }

    fn visit_item_impl(&mut self, item: &'ast ItemImpl) {
        self.owner.push(normalized(&item.self_ty));
        syn::visit::visit_item_impl(self, item);
        self.owner.pop();
    }

    fn visit_impl_item_fn(&mut self, item: &'ast ImplItemFn) {
        self.signature(&item.vis, &item.sig, item.sig.ident.span());
    }

    fn visit_item_const(&mut self, item: &'ast ItemConst) {
        let declaration = format!(
            "const {} : {} = {}",
            item.ident,
            normalized(&item.ty),
            normalized(&item.expr)
        );
        self.visibility(&item.vis, item.ident.span(), declaration);
    }

    fn visit_item_static(&mut self, item: &'ast ItemStatic) {
        let declaration = format!("static {} : {}", item.ident, normalized(&item.ty));
        self.visibility(&item.vis, item.ident.span(), declaration);
    }

    fn visit_item_type(&mut self, item: &'ast ItemType) {
        let declaration = format!(
            "type {} {} = {}",
            item.ident,
            normalized(&item.generics),
            normalized(&item.ty)
        );
        self.visibility(&item.vis, item.ident.span(), declaration);
    }

    fn visit_item_mod(&mut self, item: &'ast ItemMod) {
        self.visibility(&item.vis, item.ident.span(), format!("mod {}", item.ident));
        syn::visit::visit_item_mod(self, item);
    }

    fn visit_item_use(&mut self, item: &'ast ItemUse) {
        self.visibility(
            &item.vis,
            item.use_token.span,
            format!("use {}", normalized(&item.tree)),
        );
    }

    fn visit_foreign_item_fn(&mut self, item: &'ast ForeignItemFn) {
        self.signature(&item.vis, &item.sig, item.sig.ident.span());
    }
}

fn interface_surface(sources: &[SourceFile]) -> Vec<String> {
    let mut surface = Vec::new();
    for source in sources.iter().filter(|source| source.binary_crate) {
        let mut collector = SurfaceCollector {
            relative: &source.relative,
            entries: Vec::new(),
            errors: None,
            binary_crate: source.binary_crate,
            owner: Vec::new(),
        };
        collector.visit_file(&source.syntax);
        surface.extend(collector.entries);
    }
    surface.sort();
    surface.dedup();
    surface
}

fn check_visibility(sources: &[SourceFile], errors: &mut Vec<String>) {
    for source in sources.iter().filter(|source| source.binary_crate) {
        let mut collector = SurfaceCollector {
            relative: &source.relative,
            entries: Vec::new(),
            errors: Some(errors),
            binary_crate: source.binary_crate,
            owner: Vec::new(),
        };
        collector.visit_file(&source.syntax);
    }
}

fn check_interface_baseline(root: &Path, current: &[String], errors: &mut Vec<String>) {
    let path = root.join("docs/architecture-interface.txt");
    let Ok(text) = fs::read_to_string(&path) else {
        errors.push(format!("missing interface baseline: {}", path.display()));
        return;
    };
    let expected: BTreeSet<&str> = text
        .lines()
        .filter(|line| !line.is_empty() && !line.starts_with('#'))
        .collect();
    let current: BTreeSet<&str> = current.iter().map(String::as_str).collect();
    for item in expected.difference(&current) {
        errors.push(format!(
            "public interface removed or changed without contract update: {item}"
        ));
    }
    for item in current.difference(&expected) {
        errors.push(format!(
            "public interface expanded without contract update: {item}"
        ));
    }
}

#[derive(Default)]
struct PatternVisitor {
    static_mut: Vec<usize>,
    dense_eight_ranges: Vec<usize>,
    unfinished: Vec<usize>,
}

#[derive(Default)]
struct DisplayCompletionLogVisitor {
    inside_completion: bool,
    violations: Vec<(usize, String)>,
}

impl<'ast> Visit<'ast> for DisplayCompletionLogVisitor {
    fn visit_item_fn(&mut self, item: &'ast ItemFn) {
        let previous = self.inside_completion;
        self.inside_completion = item.sig.ident == "dispatch_display_work";
        syn::visit::visit_item_fn(self, item);
        self.inside_completion = previous;
    }

    fn visit_macro(&mut self, node: &'ast Macro) {
        if self.inside_completion
            && let Some(name) = node
                .path
                .segments
                .last()
                .map(|segment| segment.ident.to_string())
            && matches!(
                name.as_str(),
                "trace" | "debug" | "info" | "warn" | "error" | "print" | "println"
            )
        {
            self.violations.push((node.path.span().start().line, name));
        }
        syn::visit::visit_macro(self, node);
    }
}

impl<'ast> Visit<'ast> for PatternVisitor {
    fn visit_item_static(&mut self, item: &'ast ItemStatic) {
        if matches!(item.mutability, syn::StaticMutability::Mut(_)) {
            self.static_mut.push(item.static_token.span.start().line);
        }
        syn::visit::visit_item_static(self, item);
    }

    fn visit_expr_range(&mut self, range: &'ast ExprRange) {
        let integer = |expression: &Option<Box<Expr>>, expected: u64| {
            expression.as_deref().is_some_and(|expression| {
                matches!(expression, Expr::Lit(ExprLit { lit: Lit::Int(value), .. }) if value.base10_parse::<u64>().is_ok_and(|value| value == expected))
            })
        };
        if integer(&range.start, 0) && integer(&range.end, 8) {
            self.dense_eight_ranges
                .push(range.limits.span().start().line);
        }
        syn::visit::visit_expr_range(self, range);
    }

    fn visit_macro(&mut self, node: &'ast Macro) {
        if node.path.segments.last().is_some_and(|segment| {
            matches!(segment.ident.to_string().as_str(), "todo" | "unimplemented")
        }) {
            self.unfinished.push(node.path.span().start().line);
        }
        syn::visit::visit_macro(self, node);
    }
}

fn check_source_patterns(root: &Path, sources: &[SourceFile], errors: &mut Vec<String>) {
    let banned_text = [
        ("MAX_CORES", "fixed CPU capacity"),
        ("start_all_cores", "firmware-driven secondary startup"),
        ("STDOUT_FILENO", "stdout syscall side path"),
    ];
    for source in sources {
        for (needle, label) in banned_text {
            if source.text.contains(needle) {
                errors.push(format!(
                    "{}: banned pattern reintroduces {label}",
                    source.relative
                ));
            }
        }
        let lowercase = source.text.to_ascii_lowercase();
        if lowercase.contains("read-only ext2")
            || lowercase.contains("read_only ext2")
            || source.text.contains("只读 ext2")
        {
            errors.push(format!(
                "{}: banned pattern reintroduces read-only filesystem dual track",
                source.relative
            ));
        }
        let mut visitor = PatternVisitor::default();
        visitor.visit_file(&source.syntax);
        for line in visitor.static_mut {
            errors.push(format!(
                "{}: static mut global state is forbidden",
                source.at(line)
            ));
        }
        for line in visitor.dense_eight_ranges {
            errors.push(format!(
                "{}: dense eight-hart iteration is forbidden",
                source.at(line)
            ));
        }
        for line in visitor.unfinished {
            errors.push(format!(
                "{}: unfinished executable path is forbidden",
                source.at(line)
            ));
        }
        if source.relative == "kernel/src/drm/device.rs" {
            let mut hot_path = DisplayCompletionLogVisitor::default();
            hot_path.visit_file(&source.syntax);
            for (line, name) in hot_path.violations {
                errors.push(format!(
                    "{}: display completion hot path must not invoke synchronous `{name}!` logging",
                    source.at(line)
                ));
            }
        }
    }

    let gpu_boot = sources
        .iter()
        .find(|source| source.relative == "kernel/src/drivers/virtio_gpu/boot.rs")
        .map(|source| source.text.as_str())
        .unwrap_or_default();
    let drm_mode = sources
        .iter()
        .find(|source| source.relative == "kernel/src/drm/mode.rs")
        .map(|source| source.text.as_str())
        .unwrap_or_default();
    if !gpu_boot.contains("let width = host_width - host_width % 8;")
        || !drm_mode.contains("width.is_multiple_of(H_GRANULARITY)")
    {
        errors.push(
            "display mode must be canonicalized once at the VirtIO boundary and consumed unchanged by DRM"
                .to_owned(),
        );
    }

    let gpu_runtime = sources
        .iter()
        .find(|source| source.relative == "kernel/src/drivers/virtio_gpu.rs")
        .map(|source| source.text.as_str())
        .unwrap_or_default();
    let gpu_damage = sources
        .iter()
        .find(|source| source.relative == "kernel/src/drivers/virtio_gpu/damage.rs")
        .map(|source| source.text.as_str())
        .unwrap_or_default();
    let gpu_resources = sources
        .iter()
        .find(|source| source.relative == "kernel/src/drivers/virtio_gpu/resource.rs")
        .map(|source| source.text.as_str())
        .unwrap_or_default();
    if !gpu_resources.contains("target.synchronized(&control.resources)")
        || gpu_runtime.contains("UnrefReplaced")
        || !gpu_damage.contains("fn publish_next(")
        || !gpu_damage.contains("usize::from(free_descriptors / 4)")
        || !gpu_damage.contains("queue.add_to_avail(command.head)")
        || !gpu_resources.contains("slots: [Option<ResidentResource>; 2]")
        || !gpu_resources.contains("fn release_resident(")
    {
        errors.push(
            "VirtIO-GPU runtime must retain the two-slot framebuffer residency cache, batch independent DIRTYFB transfers with capacity proof, and explicitly release RMFB resources"
                .to_owned(),
        );
    }

    let garbage = [
        "common", "utils", "helpers", "misc", "manager", "base", "shared", "core",
    ];
    for domain in SOURCE_DOMAINS {
        collect_garbage_directories(&root.join(domain.root), root, &garbage, errors);
    }
}

const READY_TRANSITION_CONSUMER: &str = "consume_ready_projection_parts";
const READY_RETIREMENT_CONSUMER: &str = "consume_ready_projection_cpu";
const READY_MEMBERSHIP_PATH: &str = "kernel/src/task/processor/ready_membership.rs";
const PROCESSOR_PATH: &str = "kernel/src/task/processor.rs";
const READY_TRANSITION_COMMIT: &str = "commit_ready_transition";
const READY_RETIREMENT_COMMIT: &str = "commit_ready_retirement";
const READY_COMMIT_IMPORT: &str =
    "ready_membership :: { commit_ready_retirement , commit_ready_transition }";

#[derive(Default)]
struct ReadyProjectionConsumerVisitor {
    scope: Vec<String>,
    uses: Vec<(usize, String, Option<String>)>,
    commit_definitions: Vec<(usize, String, String)>,
    commit_shadows: Vec<(usize, String)>,
    commit_imports: Vec<(usize, String)>,
}

impl ReadyProjectionConsumerVisitor {
    fn visit_function(&mut self, name: String, body: &syn::Block) {
        self.scope.push(name);
        self.visit_block(body);
        self.scope.pop();
    }

    fn record(&mut self, line: usize, name: &str) {
        if matches!(name, READY_TRANSITION_CONSUMER | READY_RETIREMENT_CONSUMER) {
            let caller = (!self.scope.is_empty()).then(|| self.scope.join("::"));
            self.uses.push((line, name.to_owned(), caller));
        }
    }

    fn record_commit_definition(&mut self, line: usize, name: &str) {
        if matches!(name, READY_TRANSITION_COMMIT | READY_RETIREMENT_COMMIT) {
            let mut path = self.scope.clone();
            path.push(name.to_owned());
            self.commit_definitions
                .push((line, name.to_owned(), path.join("::")));
        }
    }

    fn record_commit_shadow(&mut self, line: usize, name: &str) {
        if matches!(name, READY_TRANSITION_COMMIT | READY_RETIREMENT_COMMIT) {
            self.commit_shadows.push((line, name.to_owned()));
        }
    }
}

impl<'ast> Visit<'ast> for ReadyProjectionConsumerVisitor {
    fn visit_item_fn(&mut self, item: &'ast ItemFn) {
        let name = item.sig.ident.to_string();
        self.record_commit_definition(item.sig.ident.span().start().line, &name);
        self.visit_function(name, &item.block);
    }

    fn visit_impl_item_fn(&mut self, item: &'ast ImplItemFn) {
        let name = item.sig.ident.to_string();
        self.record_commit_definition(item.sig.ident.span().start().line, &name);
        self.visit_function(name, &item.block);
    }

    fn visit_item_impl(&mut self, item: &'ast ItemImpl) {
        self.scope
            .push(format!("impl {}", normalized(&item.self_ty)));
        syn::visit::visit_item_impl(self, item);
        self.scope.pop();
    }

    fn visit_item_mod(&mut self, item: &'ast ItemMod) {
        if item.content.is_some() {
            self.scope.push(format!("mod {}", item.ident));
            syn::visit::visit_item_mod(self, item);
            self.scope.pop();
        }
    }

    fn visit_item_const(&mut self, item: &'ast ItemConst) {
        self.record_commit_shadow(item.ident.span().start().line, &item.ident.to_string());
        syn::visit::visit_item_const(self, item);
    }

    fn visit_item_static(&mut self, item: &'ast ItemStatic) {
        self.record_commit_shadow(item.ident.span().start().line, &item.ident.to_string());
        syn::visit::visit_item_static(self, item);
    }

    fn visit_item_struct(&mut self, item: &'ast ItemStruct) {
        self.record_commit_shadow(item.ident.span().start().line, &item.ident.to_string());
        syn::visit::visit_item_struct(self, item);
    }

    fn visit_pat_ident(&mut self, pattern: &'ast PatIdent) {
        self.record_commit_shadow(
            pattern.ident.span().start().line,
            &pattern.ident.to_string(),
        );
        syn::visit::visit_pat_ident(self, pattern);
    }

    fn visit_item_use(&mut self, item: &'ast ItemUse) {
        let import = normalized(&item.tree);
        if import.contains(READY_TRANSITION_COMMIT) || import.contains(READY_RETIREMENT_COMMIT) {
            self.commit_imports
                .push((item.use_token.span.start().line, import));
        }
        syn::visit::visit_item_use(self, item);
    }

    fn visit_expr_method_call(&mut self, call: &'ast syn::ExprMethodCall) {
        self.record(call.method.span().start().line, &call.method.to_string());
        syn::visit::visit_expr_method_call(self, call);
    }

    fn visit_expr_path(&mut self, path: &'ast syn::ExprPath) {
        for segment in &path.path.segments {
            self.record(
                segment.ident.span().start().line,
                &segment.ident.to_string(),
            );
        }
        syn::visit::visit_expr_path(self, path);
    }

    fn visit_use_tree(&mut self, tree: &'ast UseTree) {
        match tree {
            UseTree::Path(path) => {
                self.record(path.ident.span().start().line, &path.ident.to_string())
            }
            UseTree::Name(name) => {
                self.record(name.ident.span().start().line, &name.ident.to_string())
            }
            UseTree::Rename(rename) => {
                self.record(rename.ident.span().start().line, &rename.ident.to_string())
            }
            UseTree::Glob(_) | UseTree::Group(_) => {}
        }
        syn::visit::visit_use_tree(self, tree);
    }

    fn visit_macro(&mut self, node: &'ast Macro) {
        let tokens = node.tokens.to_string();
        for name in [READY_TRANSITION_CONSUMER, READY_RETIREMENT_CONSUMER] {
            if tokens.contains(name) {
                self.record(node.path.span().start().line, name);
            }
        }
        syn::visit::visit_macro(self, node);
    }
}

fn method_call_on<'a>(
    expression: &'a Expr,
    receiver: &str,
    method: &str,
) -> Option<&'a syn::ExprMethodCall> {
    let Expr::MethodCall(call) = expression else {
        return None;
    };
    let Expr::Path(path) = call.receiver.as_ref() else {
        return None;
    };
    (call.method == method && path.path.is_ident(receiver)).then_some(call)
}

fn ready_commit_consumes_eagerly(function: &ItemFn) -> bool {
    match function.sig.ident.to_string().as_str() {
        READY_TRANSITION_COMMIT => function.block.stmts.first().is_some_and(|statement| {
            let syn::Stmt::Local(local) = statement else {
                return false;
            };
            local.init.as_ref().is_some_and(|initialization| {
                method_call_on(
                    &initialization.expr,
                    "transition",
                    READY_TRANSITION_CONSUMER,
                )
                .is_some()
            })
        }),
        READY_RETIREMENT_COMMIT => function.block.stmts.first().is_some_and(|statement| {
            let syn::Stmt::Expr(Expr::Call(call), _) = statement else {
                return false;
            };
            let Expr::Path(callee) = call.func.as_ref() else {
                return false;
            };
            callee.path.is_ident("retire")
                && call.args.len() == 1
                && method_call_on(
                    call.args.first().expect("checked one retirement argument"),
                    "retirement",
                    READY_RETIREMENT_CONSUMER,
                )
                .is_some()
        }),
        _ => true,
    }
}

fn check_ready_projection_consumers(sources: &[SourceFile], errors: &mut Vec<String>) {
    let mut use_counts = BTreeMap::from([
        (READY_TRANSITION_CONSUMER, 0_usize),
        (READY_RETIREMENT_CONSUMER, 0_usize),
    ]);
    let mut definition_counts = BTreeMap::from([
        (READY_TRANSITION_COMMIT, 0_usize),
        (READY_RETIREMENT_COMMIT, 0_usize),
    ]);
    for source in sources {
        if source.relative == READY_MEMBERSHIP_PATH {
            for item in &source.syntax.items {
                let syn::Item::Fn(function) = item else {
                    continue;
                };
                let name = function.sig.ident.to_string();
                if matches!(
                    name.as_str(),
                    READY_TRANSITION_COMMIT | READY_RETIREMENT_COMMIT
                ) && !ready_commit_consumes_eagerly(function)
                {
                    errors.push(format!(
                        "{}: `{name}` must eagerly consume its parameter in the reviewed top-level statement shape",
                        source.at(function.sig.ident.span().start().line)
                    ));
                }
            }
        }
        let mut visitor = ReadyProjectionConsumerVisitor::default();
        visitor.visit_file(&source.syntax);
        for (line, consumer, caller) in visitor.uses {
            let required_caller = if consumer == READY_TRANSITION_CONSUMER {
                READY_TRANSITION_COMMIT
            } else {
                READY_RETIREMENT_COMMIT
            };
            if source.relative == READY_MEMBERSHIP_PATH
                && caller.as_deref() == Some(required_caller)
            {
                *use_counts
                    .get_mut(consumer.as_str())
                    .expect("Ready consumer count must exist") += 1;
            } else {
                errors.push(format!(
                    "{}: `{consumer}` may only be called by {READY_MEMBERSHIP_PATH}::{required_caller}",
                    source.at(line)
                ));
            }
        }
        for (line, name, path) in visitor.commit_definitions {
            if source.relative == READY_MEMBERSHIP_PATH && path == name {
                *definition_counts
                    .get_mut(name.as_str())
                    .expect("Ready commit definition count must exist") += 1;
            } else {
                errors.push(format!(
                    "{}: `{name}` must be the unique top-level definition in {READY_MEMBERSHIP_PATH}",
                    source.at(line)
                ));
            }
        }
        for (line, name) in visitor.commit_shadows {
            errors.push(format!(
                "{}: `{name}` may not shadow a Ready projection committer",
                source.at(line)
            ));
        }
        for (line, import) in visitor.commit_imports {
            if source.relative != PROCESSOR_PATH || import != READY_COMMIT_IMPORT {
                errors.push(format!(
                    "{}: Ready committers may only use the reviewed `{READY_COMMIT_IMPORT}` import",
                    source.at(line)
                ));
            }
        }
    }
    for (consumer, count) in use_counts {
        if count != 1 {
            errors.push(format!(
                "{READY_MEMBERSHIP_PATH}: expected exactly one `{consumer}` call, found {count}"
            ));
        }
    }
    for (commit, count) in definition_counts {
        if count != 1 {
            errors.push(format!(
                "{READY_MEMBERSHIP_PATH}: expected exactly one top-level `{commit}` definition, found {count}"
            ));
        }
    }
}

const READY_INGRESS: &str = "transition_to_ready";
const READY_RUNNING_EGRESS: &str = "transition_ready_to_running";
const READY_STOPPED_EGRESS: &str = "transition_ready_to_stopped";

type ReadyTransitionUse = (usize, usize, String);

#[derive(Default)]
struct ReadyTransitionShapeVisitor {
    uses: Vec<ReadyTransitionUse>,
    direct_commits: BTreeSet<ReadyTransitionUse>,
    malformed_commits: Vec<(usize, String)>,
    glob_imports: Vec<(usize, String)>,
}

impl ReadyTransitionShapeVisitor {
    fn is_transition(name: &str) -> bool {
        matches!(
            name,
            READY_INGRESS | READY_RUNNING_EGRESS | READY_STOPPED_EGRESS
        )
    }

    fn direct_transition(expression: &Expr) -> Option<ReadyTransitionUse> {
        match expression {
            Expr::MethodCall(call) => {
                let name = call.method.to_string();
                Self::is_transition(&name).then(|| {
                    let start = call.method.span().start();
                    (start.line, start.column, name)
                })
            }
            Expr::Call(call) => match call.func.as_ref() {
                Expr::Path(path) => path.path.segments.last().and_then(|segment| {
                    let name = segment.ident.to_string();
                    Self::is_transition(&name).then(|| {
                        let start = segment.ident.span().start();
                        (start.line, start.column, name)
                    })
                }),
                _ => None,
            },
            _ => None,
        }
    }

    fn record_ident(&mut self, identifier: &syn::Ident) {
        let name = identifier.to_string();
        if Self::is_transition(&name) {
            let start = identifier.span().start();
            self.uses.push((start.line, start.column, name));
        }
    }
}

impl<'ast> Visit<'ast> for ReadyTransitionShapeVisitor {
    fn visit_item_use(&mut self, item: &'ast ItemUse) {
        let import = normalized(&item.tree);
        if import.contains('*') {
            self.glob_imports
                .push((item.use_token.span.start().line, import));
        }
        syn::visit::visit_item_use(self, item);
    }

    fn visit_expr_call(&mut self, call: &'ast syn::ExprCall) {
        let commit = match call.func.as_ref() {
            Expr::Path(path) if path.path.segments.len() == 1 => path
                .path
                .segments
                .last()
                .map(|segment| segment.ident.to_string()),
            _ => None,
        };
        if matches!(
            commit.as_deref(),
            Some(READY_TRANSITION_COMMIT | READY_RETIREMENT_COMMIT)
        ) {
            let transition = call.args.first().and_then(Self::direct_transition);
            let expected = if commit.as_deref() == Some(READY_TRANSITION_COMMIT) {
                READY_INGRESS
            } else {
                "Ready egress"
            };
            let matches_commit = transition.as_ref().is_some_and(|(_, _, name)| {
                if commit.as_deref() == Some(READY_TRANSITION_COMMIT) {
                    name == READY_INGRESS
                } else {
                    matches!(name.as_str(), READY_RUNNING_EGRESS | READY_STOPPED_EGRESS)
                }
            });
            if matches_commit {
                self.direct_commits
                    .insert(transition.expect("checked direct transition"));
            } else {
                self.malformed_commits
                    .push((call.span().start().line, expected.to_owned()));
            }
        }
        syn::visit::visit_expr_call(self, call);
    }

    fn visit_expr_method_call(&mut self, call: &'ast syn::ExprMethodCall) {
        self.record_ident(&call.method);
        syn::visit::visit_expr_method_call(self, call);
    }

    fn visit_expr_path(&mut self, path: &'ast syn::ExprPath) {
        for segment in &path.path.segments {
            self.record_ident(&segment.ident);
        }
        syn::visit::visit_expr_path(self, path);
    }

    fn visit_use_tree(&mut self, tree: &'ast UseTree) {
        match tree {
            UseTree::Path(path) => self.record_ident(&path.ident),
            UseTree::Name(name) => self.record_ident(&name.ident),
            UseTree::Rename(rename) => self.record_ident(&rename.ident),
            UseTree::Glob(_) | UseTree::Group(_) => {}
        }
        syn::visit::visit_use_tree(self, tree);
    }

    fn visit_macro(&mut self, node: &'ast Macro) {
        let tokens = node.tokens.to_string();
        for name in [READY_INGRESS, READY_RUNNING_EGRESS, READY_STOPPED_EGRESS] {
            if tokens.contains(name) {
                let start = node.path.span().start();
                self.uses.push((start.line, start.column, name.to_owned()));
            }
        }
        syn::visit::visit_macro(self, node);
    }
}

fn check_ready_transition_shapes(sources: &[SourceFile], errors: &mut Vec<String>) {
    for source in sources {
        let mut visitor = ReadyTransitionShapeVisitor::default();
        visitor.visit_file(&source.syntax);
        if !visitor.uses.is_empty() {
            let reviewed_super_glob = matches!(
                source.relative.as_str(),
                "kernel/src/task/processor/job_control.rs"
                    | "kernel/src/task/processor/placement.rs"
                    | "kernel/src/task/processor/ready_queue.rs"
            );
            for (line, import) in &visitor.glob_imports {
                if !reviewed_super_glob || import != "super :: *" {
                    errors.push(format!(
                        "{}: Ready transitions may only inherit committers through the reviewed `super::*` processor import",
                        source.at(*line)
                    ));
                }
            }
        }
        for (line, expected) in visitor.malformed_commits {
            errors.push(format!(
                "{}: Ready commit must directly consume {expected}",
                source.at(line)
            ));
        }
        for transition in visitor.uses {
            if !visitor.direct_commits.contains(&transition) {
                let (line, _, name) = transition;
                errors.push(format!(
                    "{}: `{name}` must be a direct argument of its Ready projection commit",
                    source.at(line)
                ));
            }
        }
    }
}

#[derive(Default)]
struct PathNameVisitor {
    names: BTreeMap<String, BTreeSet<usize>>,
}

impl<'ast> Visit<'ast> for PathNameVisitor {
    fn visit_path(&mut self, path: &'ast SynPath) {
        for segment in &path.segments {
            let name = segment.ident.to_string();
            self.names
                .entry(name)
                .or_default()
                .insert(segment.ident.span().start().line);
        }
        syn::visit::visit_path(self, path);
    }

    fn visit_use_tree(&mut self, tree: &'ast UseTree) {
        let identifier = match tree {
            UseTree::Path(path) => Some(&path.ident),
            UseTree::Name(name) => Some(&name.ident),
            UseTree::Rename(rename) => Some(&rename.ident),
            UseTree::Glob(_) | UseTree::Group(_) => None,
        };
        if let Some(identifier) = identifier {
            self.names
                .entry(identifier.to_string())
                .or_default()
                .insert(identifier.span().start().line);
        }
        syn::visit::visit_use_tree(self, tree);
    }
}

fn type_contains_path(ty: &syn::Type, name: &str) -> bool {
    let mut visitor = PathNameVisitor::default();
    visitor.visit_type(ty);
    visitor.names.contains_key(name)
}

struct PersistentFallibleMapVisitor<'a> {
    relative: &'a str,
    owners: Vec<String>,
    entries: BTreeMap<String, String>,
    aliases: Vec<usize>,
}

impl PersistentFallibleMapVisitor<'_> {
    fn owner(&self) -> String {
        self.owners.join("::")
    }

    fn record(&mut self, identity: String, ty: &syn::Type) {
        if !type_contains_path(ty, "FallibleMap") {
            return;
        }
        let location = format!("{} :: {identity}", self.relative);
        let previous = self.entries.insert(location.clone(), normalized(ty));
        assert!(
            previous.is_none(),
            "duplicate persistent map identity: {location}"
        );
    }

    fn record_fields(&mut self, fields: &syn::Fields, prefix: &str) {
        for (index, field) in fields.iter().enumerate() {
            let identity = field.ident.as_ref().map_or_else(
                || format!("{prefix}[{index}]"),
                |name| format!("{prefix}.{name}"),
            );
            self.record(identity, &field.ty);
        }
    }
}

impl<'ast> Visit<'ast> for PersistentFallibleMapVisitor<'_> {
    fn visit_item_mod(&mut self, item: &'ast ItemMod) {
        if item.content.is_some() {
            self.owners.push(item.ident.to_string());
            syn::visit::visit_item_mod(self, item);
            self.owners.pop();
        }
    }

    fn visit_item_struct(&mut self, item: &'ast ItemStruct) {
        self.owners.push(item.ident.to_string());
        self.record_fields(&item.fields, &self.owner());
        self.owners.pop();
    }

    fn visit_item_union(&mut self, item: &'ast ItemUnion) {
        self.owners.push(item.ident.to_string());
        self.record_fields(&syn::Fields::Named(item.fields.clone()), &self.owner());
        self.owners.pop();
    }

    fn visit_item_enum(&mut self, item: &'ast ItemEnum) {
        self.owners.push(item.ident.to_string());
        let owner = self.owner();
        for variant in &item.variants {
            self.record_fields(&variant.fields, &format!("{owner}::{}", variant.ident));
        }
        self.owners.pop();
    }

    fn visit_item_static(&mut self, item: &'ast ItemStatic) {
        self.record(format!("static {}", item.ident), &item.ty);
    }

    fn visit_item_type(&mut self, item: &'ast ItemType) {
        if type_contains_path(&item.ty, "FallibleMap") {
            self.aliases.push(item.ident.span().start().line);
        }
    }

    fn visit_item_use(&mut self, item: &'ast ItemUse) {
        fn hides_fallible_map(tree: &UseTree, in_fallible_tree: bool) -> bool {
            match tree {
                UseTree::Path(path) => hides_fallible_map(
                    &path.tree,
                    in_fallible_tree || path.ident == "fallible_tree",
                ),
                UseTree::Rename(rename) => rename.ident == "FallibleMap",
                UseTree::Glob(_) => in_fallible_tree,
                UseTree::Group(group) => group
                    .items
                    .iter()
                    .any(|tree| hides_fallible_map(tree, in_fallible_tree)),
                UseTree::Name(_) => false,
            }
        }

        if hides_fallible_map(&item.tree, false) {
            self.aliases.push(item.use_token.span.start().line);
        }
    }

    // Function-local scratch maps are transactions, not persistent owner fields.
    fn visit_item_fn(&mut self, _item: &'ast ItemFn) {}

    fn visit_impl_item_fn(&mut self, _item: &'ast ImplItemFn) {}
}

const FALLIBLE_MAP_REGISTRY_HEADING: &str = "### Persistent FallibleMap registry";

fn registered_fallible_maps(root: &Path) -> Result<BTreeMap<String, String>, String> {
    let path = root.join("docs/architecture-contract.md");
    let text = fs::read_to_string(&path)
        .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
    let section = text
        .split_once(FALLIBLE_MAP_REGISTRY_HEADING)
        .map(|(_, section)| section)
        .ok_or_else(|| {
            format!("docs/architecture-contract.md lacks `{FALLIBLE_MAP_REGISTRY_HEADING}`")
        })?;
    let mut entries = BTreeMap::new();
    for line in section.lines().skip(1) {
        if line.starts_with("## ") || line.starts_with("### ") {
            break;
        }
        if !line.starts_with('|') || line.contains("---") || line.contains("Location") {
            continue;
        }
        let columns: Vec<&str> = line.split('|').map(str::trim).collect();
        if columns.len() != 4
            || !columns[1].starts_with('`')
            || !columns[1].ends_with('`')
            || !columns[2].starts_with('`')
            || !columns[2].ends_with('`')
        {
            return Err(format!(
                "malformed persistent FallibleMap registry row: {line}"
            ));
        }
        let location = columns[1].trim_matches('`').to_owned();
        let ty = columns[2].trim_matches('`').to_owned();
        if !location.starts_with("kernel/src/") || !ty.contains("FallibleMap") {
            return Err(format!(
                "invalid persistent FallibleMap registry row: {line}"
            ));
        }
        if entries.insert(location.clone(), ty).is_some() {
            return Err(format!(
                "duplicate persistent FallibleMap registry entry: {location}"
            ));
        }
    }
    Ok(entries)
}

#[derive(Default)]
struct FallibleTreeAllocationVisitor {
    current_function: Option<String>,
    node_allocations: Vec<(String, usize)>,
    forbidden: Vec<(usize, String)>,
    calls: BTreeMap<String, BTreeSet<String>>,
}

impl FallibleTreeAllocationVisitor {
    fn visit_function(&mut self, name: String, body: &syn::Block) {
        let previous = self.current_function.replace(name);
        self.visit_block(body);
        self.current_function = previous;
    }

    fn record_forbidden(&mut self, line: usize, detail: impl Into<String>) {
        self.forbidden.push((line, detail.into()));
    }

    fn record_call(&mut self, callee: String) {
        if let Some(caller) = &self.current_function {
            self.calls.entry(caller.clone()).or_default().insert(callee);
        }
    }

    fn record_heap_type(&mut self, identifier: &syn::Ident) {
        const FORBIDDEN_HEAP_TYPES: &[&str] = &[
            "Arc",
            "BTreeMap",
            "BTreeSet",
            "BinaryHeap",
            "HashMap",
            "HashSet",
            "LinkedList",
            "Rc",
            "String",
            "Vec",
            "VecDeque",
        ];
        let name = identifier.to_string();
        if FORBIDDEN_HEAP_TYPES.contains(&name.as_str()) {
            self.record_forbidden(
                identifier.span().start().line,
                format!("heap-backed type `{name}`"),
            );
        }
    }
}

impl<'ast> Visit<'ast> for FallibleTreeAllocationVisitor {
    fn visit_item_fn(&mut self, item: &'ast ItemFn) {
        self.visit_function(item.sig.ident.to_string(), &item.block);
    }

    fn visit_impl_item_fn(&mut self, item: &'ast ImplItemFn) {
        self.visit_function(item.sig.ident.to_string(), &item.block);
    }

    fn visit_path(&mut self, path: &'ast SynPath) {
        for segment in &path.segments {
            self.record_heap_type(&segment.ident);
        }
        syn::visit::visit_path(self, path);
    }

    fn visit_use_tree(&mut self, tree: &'ast UseTree) {
        match tree {
            UseTree::Path(path) => self.record_heap_type(&path.ident),
            UseTree::Name(name) => self.record_heap_type(&name.ident),
            UseTree::Rename(rename) => {
                self.record_heap_type(&rename.ident);
                let original = rename.ident.to_string();
                if original == "Box"
                    || matches!(original.as_str(), "alloc" | "alloc_zeroed" | "realloc")
                {
                    self.record_forbidden(
                        rename.ident.span().start().line,
                        format!("aliased allocator `{original}`"),
                    );
                }
            }
            UseTree::Glob(glob) => self.record_forbidden(
                glob.star_token.span.start().line,
                "glob import with opaque allocation surface",
            ),
            UseTree::Group(_) => {}
        }
        syn::visit::visit_use_tree(self, tree);
    }

    fn visit_expr_call(&mut self, call: &'ast syn::ExprCall) {
        if let Expr::Path(function) = call.func.as_ref() {
            let segments: Vec<_> = function.path.segments.iter().collect();
            let first = segments.first().map(|segment| segment.ident.to_string());
            let last = segments.last().map(|segment| segment.ident.to_string());
            if let Some(last) = &last {
                self.record_call(last.clone());
            }
            if first.as_deref() == Some("Box") {
                match last.as_deref() {
                    Some("try_new_uninit") if normalized(&function.path).contains("Node") => {
                        self.node_allocations.push((
                            self.current_function
                                .clone()
                                .unwrap_or_else(|| "<unknown>".to_owned()),
                            function.path.span().start().line,
                        ));
                    }
                    Some(
                        method @ ("new" | "new_uninit" | "new_zeroed" | "pin" | "try_new"
                        | "try_new_zeroed" | "try_pin"),
                    ) => self.record_forbidden(
                        function.path.span().start().line,
                        format!("Box::{method}"),
                    ),
                    _ => {}
                }
            }
            if matches!(last.as_deref(), Some("alloc" | "alloc_zeroed" | "realloc")) {
                self.record_forbidden(
                    function.path.span().start().line,
                    normalized(&function.path),
                );
            }
        }
        syn::visit::visit_expr_call(self, call);
    }

    fn visit_expr_method_call(&mut self, call: &'ast syn::ExprMethodCall) {
        let method = call.method.to_string();
        self.record_call(method.clone());
        if matches!(
            method.as_str(),
            "collect"
                | "extend"
                | "extend_from_slice"
                | "reserve"
                | "reserve_exact"
                | "resize"
                | "shrink_to"
                | "shrink_to_fit"
                | "to_string"
                | "to_vec"
                | "try_reserve"
                | "try_reserve_exact"
        ) {
            self.record_forbidden(call.method.span().start().line, format!(".{method}()"));
        }
        syn::visit::visit_expr_method_call(self, call);
    }

    fn visit_macro(&mut self, node: &'ast Macro) {
        let name = node
            .path
            .segments
            .last()
            .map(|segment| segment.ident.to_string())
            .unwrap_or_default();
        if !matches!(name.as_str(), "assert" | "debug_assert") {
            self.record_forbidden(
                node.path.span().start().line,
                format!("opaque `{name}!` macro"),
            );
        }
        syn::visit::visit_macro(self, node);
    }
}

fn check_fallible_collections(root: &Path, sources: &[SourceFile], errors: &mut Vec<String>) {
    let mut actual = BTreeMap::new();
    for source in sources
        .iter()
        .filter(|source| source.relative.starts_with("kernel/src/"))
    {
        let mut paths = PathNameVisitor::default();
        paths.visit_file(&source.syntax);
        for name in ["BTreeMap", "BTreeSet"] {
            if let Some(lines) = paths.names.get(name) {
                for line in lines {
                    errors.push(format!(
                        "{}: kernel must use the fallible ordered-map seam, not `{name}`",
                        source.at(*line)
                    ));
                }
            }
        }

        let mut visitor = PersistentFallibleMapVisitor {
            relative: &source.relative,
            owners: Vec::new(),
            entries: BTreeMap::new(),
            aliases: Vec::new(),
        };
        visitor.visit_file(&source.syntax);
        for line in visitor.aliases {
            errors.push(format!(
                "{}: persistent FallibleMap aliases bypass the exact owner registry",
                source.at(line)
            ));
        }
        for (location, ty) in visitor.entries {
            if actual.insert(location.clone(), ty).is_some() {
                errors.push(format!(
                    "duplicate persistent FallibleMap owner: {location}"
                ));
            }
        }
    }

    match registered_fallible_maps(root) {
        Ok(registered) => {
            for (location, expected_type) in &registered {
                match actual.get(location) {
                    None => errors.push(format!(
                        "registered persistent FallibleMap was removed or replaced: {location}"
                    )),
                    Some(actual_type) if actual_type != expected_type => errors.push(format!(
                        "persistent FallibleMap type changed at {location}: expected `{expected_type}`, found `{actual_type}`"
                    )),
                    Some(_) => {}
                }
            }
            for (location, ty) in &actual {
                if !registered.contains_key(location) {
                    errors.push(format!(
                        "persistent FallibleMap lacks architecture contract entry: {location} :: `{ty}`"
                    ));
                }
            }
        }
        Err(error) => errors.push(error),
    }

    let mut allocation_sites = Vec::new();
    let mut calls: BTreeMap<String, BTreeSet<String>> = BTreeMap::new();
    for source in sources.iter().filter(|source| {
        source.relative == "kernel/src/fallible_tree.rs"
            || source.relative.starts_with("kernel/src/fallible_tree/")
    }) {
        let mut visitor = FallibleTreeAllocationVisitor::default();
        visitor.visit_file(&source.syntax);
        for (line, detail) in visitor.forbidden {
            errors.push(format!(
                "{}: fallible_tree topology/iteration must not allocate through {detail}",
                source.at(line)
            ));
        }
        allocation_sites.extend(
            visitor
                .node_allocations
                .into_iter()
                .map(|(function, line)| (source.relative.clone(), function, line)),
        );
        for (caller, callees) in visitor.calls {
            calls.entry(caller).or_default().extend(callees);
        }
    }
    if allocation_sites.len() != 1
        || allocation_sites
            .first()
            .is_none_or(|(source, function, _)| {
                source != "kernel/src/fallible_tree.rs" || function != "try_reserve_node"
            })
    {
        let sites = allocation_sites
            .iter()
            .map(|(source, function, line)| format!("{source}:{line}::{function}"))
            .collect::<Vec<_>>()
            .join(", ");
        errors.push(format!(
            "fallible_tree must have exactly one Box::try_new_uninit node allocation in try_reserve_node; found [{sites}]"
        ));
    }

    // 1. 从唯一 allocation primitive 反向闭包全部内部 caller。
    // 2. 只允许四个显式 fallible prepare/insert API 可达该 primitive。
    // 3. rotation/remove/retain/iteration 若直接或间接接入分配，都会成为额外 caller 并失败。
    let mut allocating = BTreeSet::from(["try_reserve_node".to_owned()]);
    loop {
        let previous = allocating.len();
        for (caller, callees) in &calls {
            if callees.iter().any(|callee| allocating.contains(callee)) {
                allocating.insert(caller.clone());
            }
        }
        if allocating.len() == previous {
            break;
        }
    }
    let expected_allocating = BTreeSet::from([
        "try_insert".to_owned(),
        "try_prepare".to_owned(),
        "try_prepare_vacant".to_owned(),
        "try_reserve_node".to_owned(),
    ]);
    if allocating != expected_allocating {
        errors.push(format!(
            "fallible_tree allocation call graph changed: expected {expected_allocating:?}, found {allocating:?}"
        ));
    }
}

fn collect_garbage_directories(
    directory: &Path,
    root: &Path,
    garbage: &[&str],
    errors: &mut Vec<String>,
) {
    let Ok(entries) = fs::read_dir(directory) else {
        return;
    };
    for entry in entries.flatten() {
        let path = entry.path();
        if !path.is_dir() {
            continue;
        }
        if path
            .file_name()
            .and_then(|name| name.to_str())
            .is_some_and(|name| garbage.contains(&name))
        {
            errors.push(format!(
                "{}: directory name has no domain meaning",
                path.strip_prefix(root).unwrap_or(&path).display()
            ));
        }
        collect_garbage_directories(&path, root, garbage, errors);
    }
}

#[derive(Default)]
struct GlobalVisitor {
    lines: Vec<usize>,
}

impl<'ast> Visit<'ast> for GlobalVisitor {
    fn visit_item_static(&mut self, item: &'ast ItemStatic) {
        self.lines.push(item.static_token.span.start().line);
    }
}

fn check_global_owners(sources: &[SourceFile], errors: &mut Vec<String>) {
    for source in sources.iter().filter(|source| source.binary_crate) {
        let mut visitor = GlobalVisitor::default();
        visitor.visit_file(&source.syntax);
        for (index, line) in source.lines.iter().enumerate() {
            if line.contains("static ref ") {
                visitor.lines.push(index + 1);
            }
        }
        visitor.lines.sort_unstable();
        visitor.lines.dedup();
        for line in visitor.lines {
            if !source.preceding_contains(line, 4, "OWNER:") {
                errors.push(format!(
                    "{}: global state lacks an OWNER declaration",
                    source.at(line)
                ));
            }
        }
    }
}

#[derive(Default)]
struct UnsafeVisitor {
    lines: Vec<usize>,
}

impl UnsafeVisitor {
    fn signature(&mut self, signature: &Signature) {
        if let Some(unsafety) = signature.unsafety {
            self.lines.push(unsafety.span.start().line);
        }
    }
}

impl<'ast> Visit<'ast> for UnsafeVisitor {
    fn visit_expr_unsafe(&mut self, expression: &'ast syn::ExprUnsafe) {
        self.lines.push(expression.unsafe_token.span.start().line);
        syn::visit::visit_expr_unsafe(self, expression);
    }

    fn visit_item_impl(&mut self, item: &'ast ItemImpl) {
        if let Some(unsafety) = item.unsafety {
            self.lines.push(unsafety.span.start().line);
        }
        syn::visit::visit_item_impl(self, item);
    }

    fn visit_item_fn(&mut self, item: &'ast ItemFn) {
        self.signature(&item.sig);
        syn::visit::visit_item_fn(self, item);
    }

    fn visit_impl_item_fn(&mut self, item: &'ast ImplItemFn) {
        self.signature(&item.sig);
        syn::visit::visit_impl_item_fn(self, item);
    }

    fn visit_foreign_item_fn(&mut self, item: &'ast ForeignItemFn) {
        self.signature(&item.sig);
        syn::visit::visit_foreign_item_fn(self, item);
    }

    fn visit_item_foreign_mod(&mut self, item: &'ast ItemForeignMod) {
        self.lines.push(item.abi.extern_token.span.start().line);
        syn::visit::visit_item_foreign_mod(self, item);
    }
}

fn check_unsafe_proofs(sources: &[SourceFile], errors: &mut Vec<String>) {
    for source in sources {
        let mut visitor = UnsafeVisitor::default();
        visitor.visit_file(&source.syntax);
        visitor.lines.sort_unstable();
        visitor.lines.dedup();
        for line in visitor.lines {
            if !source.preceding_contains(line, 6, "SAFETY:") {
                errors.push(format!(
                    "{}: unsafe operation lacks a local SAFETY proof",
                    source.at(line)
                ));
            }
        }
    }
}

fn syscall_constants(file: &File) -> BTreeSet<String> {
    file.items
        .iter()
        .filter_map(|item| match item {
            syn::Item::Const(item) if item.ident.to_string().starts_with("SYSCALL_") => {
                Some(item.ident.to_string())
            }
            _ => None,
        })
        .collect()
}

#[derive(Default)]
struct DispatchVisitor {
    constants: BTreeSet<String>,
    numeric_arms: Vec<usize>,
}

impl<'ast> Visit<'ast> for DispatchVisitor {
    fn visit_path(&mut self, path: &'ast SynPath) {
        for segment in &path.segments {
            let name = segment.ident.to_string();
            if name.starts_with("SYSCALL_") {
                self.constants.insert(name);
            }
        }
        syn::visit::visit_path(self, path);
    }

    fn visit_arm(&mut self, arm: &'ast Arm) {
        if matches!(
            &arm.pat,
            Pat::Lit(ExprLit {
                lit: Lit::Int(_),
                ..
            })
        ) {
            self.numeric_arms
                .push(arm.fat_arrow_token.spans[0].start().line);
        }
        syn::visit::visit_arm(self, arm);
    }

    fn visit_pat_ident(&mut self, pattern: &'ast PatIdent) {
        let name = pattern.ident.to_string();
        if name.starts_with("SYSCALL_") {
            self.constants.insert(name);
        }
        syn::visit::visit_pat_ident(self, pattern);
    }

    fn visit_expr_match(&mut self, node: &'ast ExprMatch) {
        syn::visit::visit_expr_match(self, node);
    }
}

fn check_abi(root: &Path, errors: &mut Vec<String>) {
    let abi_path = root.join("syscall-abi/src/lib.rs");
    let dispatch_path = root.join("kernel/src/syscall/mod.rs");
    let abi_text = fs::read_to_string(&abi_path).expect("syscall ABI source must exist");
    let dispatch_text = fs::read_to_string(&dispatch_path).expect("syscall dispatch must exist");
    let abi = syn::parse_file(&abi_text).expect("syscall ABI must parse");
    let dispatch = syn::parse_file(&dispatch_text).expect("syscall dispatch must parse");
    let constants = syscall_constants(&abi);
    let mut visitor = DispatchVisitor::default();
    visitor.visit_file(&dispatch);
    for name in constants.difference(&visitor.constants) {
        errors.push(format!("syscall ABI constant is not dispatched: {name}"));
    }
    for name in visitor.constants.difference(&constants) {
        errors.push(format!(
            "dispatcher uses a syscall absent from syscall-abi: {name}"
        ));
    }
    for line in visitor.numeric_arms {
        errors.push(format!(
            "kernel/src/syscall/mod.rs:{line}: raw numeric syscall dispatch is forbidden"
        ));
    }
}

fn check_documentation(root: &Path, errors: &mut Vec<String>) {
    let abi_text = fs::read_to_string(root.join("syscall-abi/src/lib.rs")).unwrap();
    let abi = syn::parse_file(&abi_text).unwrap();
    let count = syscall_constants(&abi).len();
    let readme = fs::read_to_string(root.join("README.md")).unwrap();
    if !readme.contains(&format!("{count} 个 Linux/riscv64 syscall")) {
        errors.push(format!(
            "README syscall count must match syscall-abi ({count})"
        ));
    }
    let agents = fs::read_to_string(root.join("AGENTS.md")).unwrap();
    for phrase in [
        "个 Linux/riscv64 syscall",
        "同步读写 ext2",
        "nightly-",
        "三个组件",
    ] {
        if agents.contains(phrase) {
            errors.push(format!(
                "AGENTS.md duplicates discoverable project state: {phrase}"
            ));
        }
    }
    let architecture = fs::read_to_string(root.join("docs/architecture.md")).unwrap();
    for phrase in [
        "startup stack、processor slot",
        "- fd/OFD、普通文件 I/O",
        "- writable filesystem",
        "**fd/OFD 文件竖切**",
    ] {
        if architecture.contains(phrase) {
            errors.push(format!(
                "docs/architecture.md retains superseded current-state claim: {phrase}"
            ));
        }
    }
}

